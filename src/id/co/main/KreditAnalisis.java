    private void btnBisaTidakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBisaTidakActionPerformed
        // TODO add your handling code here:
        try{
            String nama=txtNama.getText();
            //Konversi tipe data dari String ke double
            double limitPinjaman=Double.parseDouble(txtLimit.getText());
            double cicilanPerBulan=Double.parseDouble(txtAngsuran.getText());
            
            //Pemanggilan konstruktor dengan parameter kosong, ini digunakan untuk memanggil method pada class tujuan yang tidak dibuatkan parameternya
            mAnalisa=new MAnalisa();
            //Buat variable sekaligus memanggil method pada class MAnalisa
            boolean diterima=mAnalisa.isApprove(limitPinjaman, cicilanPerBulan);
            if(diterima){
                lblInformasi.setText("Kredit "+nama+" disetujui");
            }else if(mAnalisa.isLebihBesar()){
                lblInformasi.setText("Kredit "+nama+" disetujui");
            }else{
                lblInformasi.setText("Kredit "+nama+" ditolak...!");
            }
            
        }catch (Exception e){getToolkit().beep();
        JOptionPane.showMessageDialog(null,"Lengkapi dulu data yang kosong, atau isi dengan value nol...!","Pesan",JOptionPane.WARNING_MESSAGE);                 
        }
    }//GEN-LAST:event_btnBisaTidakActionPerformed

