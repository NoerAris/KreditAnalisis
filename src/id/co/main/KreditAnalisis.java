    private void btnHitungLimitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungLimitActionPerformed
        try{
            //Konversi tipe data ke double
            double gajiPokok=Double.parseDouble(txtGajiPokok.getText());
            double penghasilanTambahan=Double.parseDouble(txtPenghasilanTambahan.getText());
            double bonus=Double.parseDouble(txtBonus.getText());
            int jumlahTanggungan=cJumlahTanggungan.getSelectedIndex();      //Tipe data pada combobox dibuat bertipe int
            //String limitStr=String.format("Rp. "+"%,.2f",totalLimit);     //Konversi tipe data ke String dengan tambahan Rp dan 2 digit setelah koma
            
            //Memanggil konstruktor hitungLimitPinjaman
            mNasabah=new MNasabah(gajiPokok,penghasilanTambahan,bonus,jumlahTanggungan);
            //Perhitungan
            double hitung=mNasabah.hitungLimitPinjaman();
            //Konversi data dari double ke String
            String hasil=Double.toString(hitung);
            txtLimit.setText(hasil);
        }catch (NumberFormatException nfe){
            getToolkit().beep();
            JOptionPane.showMessageDialog(null,"Lengkapi dulu data Penghasilan yang kosong, atau isi value yang kosong dengan angka nol...!","Pesan",JOptionPane.WARNING_MESSAGE);    
        }
    }//GEN-LAST:event_btnHitungLimitActionPerformed

